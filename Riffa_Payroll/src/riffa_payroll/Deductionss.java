    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to add record?","Add Record",JOptionPane.YES_NO_OPTION);
        if(p==0){
            
            String value3 = lbl_emp.getText();
            try{
                
                String sql = "insert into deductions (first_name, surname, salary, deduction_amount, deduction_reason, emp_id, made_by) values (?,?,?,?,?,?, '"+value3+"')";
                pst = conn.prepareStatement(sql);
                pst.setString(1, txt_fnme.getText());
                pst.setString(2, txt_snme.getText());
                pst.setString(3, txt_bsc.getText());
                pst.setString(4, lbl_ttld.getText());
                pst.setString(5, txt_rsn.getText());
                pst.setString(6, txt_empid.getText());
                
                pst.execute();
                JOptionPane.showMessageDialog(null, "Deductions successfully made ");
            
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            
            }
            finally
            {
                try
                {
                    rs.close();
                    pst.close();
                
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null, e);
                
                }
            
            }
        
        
        }
     
        

    }//GEN-LAST:event_jButton1ActionPerformed

