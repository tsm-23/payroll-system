    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
       int salary = Integer.parseInt(txt_bsc.getText());
        
        if(r_pct.isSelected()== true){
        int percentage = Integer.parseInt(txt_pct.getText());
        //calculate the total hours of overtime
        int total_percentage_deduction = salary /100 * percentage;
        String x = String.valueOf(total_percentage_deduction);
        int sal = salary - total_percentage_deduction;
        lbl_ttld.setText(x);
        lbl_sad.setText(String.valueOf(sal));
         }
        
        if(r_amnt.isSelected()== true){
        int deduction = Integer.parseInt(txt_amnt.getText());
        //calculate the total hours of overtime
        int total_amount_deduction =  salary - deduction;
        String s = String.valueOf(total_amount_deduction);
       
        lbl_sad.setText(s);
        lbl_ttld.setText(String.valueOf(deduction));

} 
        
    }//GEN-LAST:event_jButton2ActionPerformed

